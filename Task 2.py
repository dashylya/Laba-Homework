m = input("введите количество команд ")
i = 1
f = 1
while i <= int(m):
    f *= i
    i += 1
o = int(m) - 3
j = 1
k = 1
while k <= o:
    j *= k
    k += 1
n = f / j
print("Вариантов распредлеления команд на первые 3 места = " + str(n))
